#include <vector>
#include <iostream>

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
template <typename T>
concept Equal_comparable = 
    requires (T a, T b) 
{
    {a == b} -> bool;
    {a != b} -> bool;
};

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
bool are_equal(Equal_comparable a, Equal_comparable b) {return a == b;}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
int main()
{
    std::cout << " are equal 1 and 5: " << are_equal(1,5) << std::endl;

    return 0;
}

